#!/usr/bin/env node
import { existsSync, mkdirSync, readdirSync, readFileSync, rmSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

// Copies packages/image-processing/src into each service's src/vendor/image-processing.
// We vendor instead of importing via npm because SAM's NodejsNpmEsbuildBuilder copies
// CodeUri to a temp dir and runs `npm install` there, which can't resolve workspace
// `file:` paths that point outside CodeUri. Vendored source sits inside CodeUri and
// esbuild bundles it normally.
//
// Services opt in by listing themselves in SERVICES below. Handlers should import
// from './vendor/image-processing/index.mjs' (or up-one-level equivalent).

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const sourceDir = join(repoRoot, 'packages', 'image-processing', 'src');

const SERVICES = [
  'services/web-api',
  'services/okra-api',
];

for (const service of SERVICES) {
  const targetDir = join(repoRoot, service, 'src', 'vendor', 'image-processing');
  if (existsSync(targetDir)) {
    rmSync(targetDir, { recursive: true, force: true });
  }
  mkdirSync(targetDir, { recursive: true });

  for (const entry of readdirSync(sourceDir)) {
    if (!entry.endsWith('.mjs')) continue;
    const contents = readFileSync(join(sourceDir, entry));
    writeFileSync(join(targetDir, entry), contents);
  }

  // Drop a one-line note so no one mistakenly edits the vendored copy.
  writeFileSync(
    join(targetDir, 'README.md'),
    '# Vendored — do not edit\n\nSource: `packages/image-processing/`. ' +
      'Regenerated by `scripts/stage-image-processing.mjs` before every SAM build.\n',
  );
}

console.log(`staged image-processing into ${SERVICES.length} service(s)`);
